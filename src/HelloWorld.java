/**
 * Displays user favorite numbers and adds them up.
 * @author Dr. Mondesire, 9/3/2020
 */
public class HelloWorld {
	/**
	 * Adds users' favorite numbers.
	 * @param args Parameters for the function.
	 */
	public static void main(String[] args) {
		// Declaring variables
		int ale = 23;
		int thom = 24;
		int ryan = 14;
		
		// Adding variables together
		int dio = ale + thom + ryan;
		
		// Outputting variables
		System.out.print("Total: " + dio);
	}

}
